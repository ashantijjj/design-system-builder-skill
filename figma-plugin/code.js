function visualColorTokens(system) {
  const tokens = [];
  system.colorTokens.forEach((token) => {
    if (token.visualModeGroups) {
      token.visualModeGroups.forEach((group) => {
        tokens.push({ ...token, value: group.value, opacity: group.opacity, name: insertTokenSegment(token.name, group.name) });
      });
      return;
    }
    if (!token.valuesByMode || system.colorModes.length <= 1) {
      tokens.push(token);
      return;
    }
    if (modeValuesAreIdentical(token, system.colorModes)) {
      tokens.push(token);
      return;
    }
    system.colorModes.forEach((modeName) => {
      const value = tokenValueForMode(token, modeName);
      tokens.push({ ...token, ...value, name: `${modeName}/${token.name}` });
    });
  });
  return [...tokens, ...system.gradientTokens];
}

function insertTokenSegment(name, segment) {
  const parts = String(name || "").split("/");
  if (parts.length < 2) return `${segment}/${name}`;
  return [parts[0], segment, ...parts.slice(1)].join("/");
}

function modeValuesAreIdentical(token, modes) {
  if (!token.valuesByMode || modes.length <= 1) return true;
  const first = visualModeValueKey(token, modes[0]);
  return modes.every((modeName) => visualModeValueKey(token, modeName) === first);
}

function visualModeValueKey(token, modeName) {
  const value = tokenValueForMode(token, modeName);
  return `${value.value || ""}|${value.opacity ?? ""}`;
}

