function isLegacyGeneratedVariable(variable, collectionById, config = {}) {
  const collection = variable?.variableCollectionId ? collectionById.get(variable.variableCollectionId) : null;
  if (collection && !isKnownGeneratedCollection(collection.name)) return false;
  const parts = stripKnownProjectPrefix(splitTokenName(variable?.name), config.projectCode);
  if (LEGACY_AUXILIARY_NAMES.has(parts.join("/"))) return true;
  const auxiliaryIndex = parts.indexOf("Auxiliary");
  if (auxiliaryIndex === -1) return false;
  const legacyName = parts.slice(auxiliaryIndex + 1).join("/");
  return LEGACY_AUXILIARY_NAMES.has(legacyName);
}

function isGeneratedVisualPage(page) {
  const cleanName = String(page?.name || "").trim();
  return isPluginOwned(page) || GENERATED_PAGE_NAMES.has(cleanName);
}

function isGeneratedDesignSystemVariable(variable, collectionById, config = {}) {
  const collection = variable?.variableCollectionId ? collectionById.get(variable.variableCollectionId) : null;
  const rawParts = splitTokenName(variable?.name);
  const hasKnownPrefix = hasKnownProjectPrefix(rawParts, config.projectCode);
  if (!hasKnownPrefix && (!collection || !isKnownGeneratedCollection(collection.name))) return false;
  const parts = stripKnownProjectPrefix(rawParts, config.projectCode);
  if (collection && collection.name === "Color" && isLegacyColorCollectionVariable(parts)) return true;
  if (collection && collection.name === "Numbers" && isLegacyNumberCollectionVariable(parts)) return true;
  return isKnownGeneratedTopLevel(parts[0]) || isLegacyColorCollectionVariable(parts) || isLegacyNumberCollectionVariable(parts);
}

function isLegacyColorCollectionVariable(parts) {
  const cleanParts = normalizeNameParts(parts);
  const name = cleanParts.join("/");
  if (/^(?:100|90|80|70|60|50|40|30|20|15|10|7|5|3|1)%$/.test(name)) return true;
  if (/^(?:Neutral|Neutrals|Grey|Gray|Black|White|Overlay)(?:\/|$)/i.test(name)) return true;
  if (/^(?:Surface|Border)(?:\/|$)/i.test(name)) return true;
  if (/^(?:Background)(?:\/|$)/i.test(name)) return true;
  if (/^(?:BG|default)$/i.test(name)) return true;
  if (/^(?:Auxiliary)(?:\/|$)/i.test(name)) return true;
  if (/^(?:aux-\d+\/)?(?:base|02|03|green|red|orange|blue|purple|Wireframes01|Wireframes02|.*-\d+%)$/i.test(name)) return true;
  return false;
}

function isLegacyNumberCollectionVariable(parts) {
  const name = normalizeNameParts(parts).join("/");
  if (/^(?:Radius|Spacing)(?:\/|$)/.test(name)) return true;
  if (/^(?:0|2|4|6|8|10|12|14|16|20|24|28|32|40|48|999)(?:px)?$/.test(name)) return true;
  return false;
}

function isGeneratedPaintStyle(style, config = {}) {
  const parts = stripKnownProjectPrefix(splitTokenName(style?.name), config.projectCode);
  if (!parts.length) return false;
  if (isKnownGeneratedPaintTopLevel(parts[0])) return true;
  return isLegacyColorCollectionVariable(parts) || LEGACY_AUXILIARY_NAMES.has(parts.join("/"));
}

function isGeneratedTextStyle(style, config = {}) {
  const parts = stripKnownProjectPrefix(splitTokenName(style?.name), config.projectCode);
  if (!parts.length) return false;
  if (isKnownGeneratedTextTopLevel(parts[0])) return true;
  if (/^[1-5]$/.test(parts[0]) && parts.length === 1) return true;
  if (isLegacyTextStyleName(parts.join("/"))) return true;
  return false;
}

function isGeneratedEffectStyle(style, config = {}) {
  const parts = stripKnownProjectPrefix(splitTokenName(style?.name), config.projectCode);
  if (!parts.length) return false;
  return isKnownGeneratedEffectTopLevel(parts[0]) || ["card", "floating", "sheet-modal"].includes(parts.join("/"));
}

function isLegacyTextStyleName(name) {
  return (
    /^(?:H[1-5]\.? ?Headline|S[1-2]\.? ?Subtitle|B[1-4]\.? ?Body|C[1-3]\.? ?Caption)$/i.test(name) ||
    /^(?:Title|Subtitle|Body|Caption|Label)(?:\/|\b|$)/i.test(name) ||
    /^(?:Giant|Large|Medium|Small|Tiny)$/i.test(name)
  );
}

function isSafeToDeleteGeneratedCollection(collection, variables, matchingVariableIds) {
  if (!isKnownGeneratedCollection(collection.name) && !isKnownLegacyCollection(collection.name)) return false;
  const children = variables.filter((variable) => variable.variableCollectionId === collection.id);
  return children.length === 0 || children.every((variable) => matchingVariableIds.has(variable.id));
}

function stripKnownProjectPrefix(parts, projectCode) {
  parts = normalizeNameParts(parts);
  if (!parts.length) return parts;
  if (projectCode && parts[0] === projectCode) return parts.slice(1);
  if (/^OOG\d+$/i.test(parts[0])) return parts.slice(1);
  return parts;
}

function hasKnownProjectPrefix(parts, projectCode) {
  parts = normalizeNameParts(parts);
  if (!parts.length) return false;
  return Boolean((projectCode && parts[0] === projectCode) || /^OOG\d+$/i.test(parts[0]));
}

function splitTokenName(name) {
  return normalizeNameParts(String(name || "").split("/"));
}

function normalizeNameParts(parts) {
  return (parts || []).map((part) => String(part || "").trim()).filter(Boolean);
}

function isKnownGeneratedTopLevel(name) {
  return hasSetValueCaseInsensitive(GENERATED_VARIABLE_TOP_LEVELS, name);
}

function isKnownGeneratedCollection(name) {
  return hasSetValueCaseInsensitive(GENERATED_VARIABLE_COLLECTIONS, name);
}

function isKnownLegacyCollection(name) {
  return hasSetValueCaseInsensitive(LEGACY_VARIABLE_COLLECTIONS, name);
}

function isKnownGeneratedPaintTopLevel(name) {
  return hasSetValueCaseInsensitive(GENERATED_PAINT_STYLE_TOP_LEVELS, name);
}

function isKnownGeneratedTextTopLevel(name) {
  return hasSetValueCaseInsensitive(GENERATED_TEXT_STYLE_TOP_LEVELS, name);
}

function isKnownGeneratedEffectTopLevel(name) {
  return hasSetValueCaseInsensitive(GENERATED_EFFECT_STYLE_TOP_LEVELS, name);
}

function hasSetValueCaseInsensitive(set, value) {
  const clean = String(value || "").trim().toLowerCase();
  return [...set].some((item) => item.toLowerCase() === clean);
}

async function snapshotExisting(existing) {
  const collections = await getLocalCollections();
  const collectionById = new Map(collections.map((collection) => [collection.id, collection]));
  const collectionIds = new Set(existing.collections.map((collection) => collection.id));
  existing.variables.forEach((variable) => {
    if (variable.variableCollectionId) collectionIds.add(variable.variableCollectionId);
  });

  return {
    collections: collections
      .filter((collection) => collectionIds.has(collection.id))
      .map((collection) => ({
        name: collection.name,
        modes: collection.modes.map((mode) => mode.name),
      })),
    variables: existing.variables.map((variable) => snapshotVariable(variable, collectionById.get(variable.variableCollectionId))),
    paintStyles: existing.paintStyles.map(snapshotPaintStyle),
    textStyles: existing.textStyles.map(snapshotTextStyle),
    effectStyles: existing.effectStyles.map(snapshotEffectStyle),
  };
}

function snapshotVariable(variable, collection) {
  const valuesByModeName = {};
  const modes = collection ? collection.modes : [];
  modes.forEach((mode) => {
    if (variable.valuesByMode && variable.valuesByMode[mode.modeId] !== undefined) {
      valuesByModeName[mode.name] = cloneSnapshotValue(variable.valuesByMode[mode.modeId]);
    }
  });

  return {
    name: variable.name,
    type: variable.resolvedType || "COLOR",
    collectionName: collection ? collection.name : "Design System",
    modes: modes.map((mode) => mode.name),
    valuesByModeName,
  };
}

