figma.ui.onmessage = async (message) => {
  if (message.type === "cancel") {
    figma.closePlugin();
    return;
  }

  if (message.type === "pick-selected-color") {
    figma.ui.postMessage({
      type: "picked-color",
      requestId: message.requestId,
      ...readSelectedSolidFill(),
    });
    return;
  }

  if (message.type === "validate-fonts") {
    const results = await validateFontRequests(message.requests || [], Boolean(message.strict));
    figma.ui.postMessage({
      type: "font-validation",
      requestId: message.requestId,
      results,
    });
    return;
  }

  if (message.type !== "generate") return;

  let restoreSnapshot = null;

  try {
    const config = normalizeConfig(message.config);
    const issues = [...validateRawInputs(message.config || {}, config), ...validateConfig(config)];
    if (issues.length) {
      figma.ui.postMessage({ type: "error", message: issues.join("\n") });
      return;
    }

    const system = buildSystem(config);
    await preloadFonts(system.textTokens);

    const existing = await scanExisting(system);
    if (existing.total > 0 && !message.confirmedOverwrite) {
      figma.ui.postMessage({
        type: "confirm-overwrite",
        message: `当前文件已有 ${existing.total} 个本地变量 / 样式或插件生成页面。是否全部删除后重新生成？`,
        existing,
      });
      return;
    }

    restoreSnapshot = message.confirmedOverwrite && existing.total > 0 ? await snapshotExisting(existing) : null;
    const pagesToDeleteAfterSuccess = message.confirmedOverwrite && existing.total > 0 ? existing.pages.slice() : [];

    if (message.confirmedOverwrite && existing.total > 0) {
      await deleteExisting(existing, { keepPages: true });
    }

    const result = await writeSystem(system);
    await deletePages(pagesToDeleteAfterSuccess);
    figma.ui.postMessage({ type: "success", message: summarizeResult(result), result });
    figma.notify("Design system generated");
  } catch (error) {
    const messageText = error && error.message ? error.message : String(error);
    const restoreMessage = await restoreAfterFailedOverwrite(restoreSnapshot);
    figma.ui.postMessage({ type: "error", message: `生成失败，已清理本次新建的半成品。${restoreMessage}\n${messageText}` });
  }
};

function readSelectedSolidFill() {
  const selection = figma.currentPage.selection || [];
  if (!selection.length) {
    return {
      ok: false,
      message: "请先在画布中选中一个带纯色填充的图层。",
    };
  }

  const node = selection[0];
  if (!("fills" in node)) {
    return {
      ok: false,
      message: "当前选中图层没有可读取的填充色。请选择一个纯色填充图层。",
    };
  }

  const fills = node.fills;
  if (fills === figma.mixed) {
    return {
      ok: false,
      message: "当前选中图层的填充是混合状态。请选择单个纯色填充图层。",
    };
  }

  if (!Array.isArray(fills) || fills.length === 0) {
    return {
      ok: false,
      message: "当前选中图层没有填充色。请选择一个纯色填充图层。",
    };
  }

  const firstVisibleFill = fills.find((paint) => paint.visible !== false);
  const firstSolidFill = fills.find((paint) => paint.visible !== false && paint.type === "SOLID");
  if (!firstVisibleFill) {
    return {
      ok: false,
      message: "当前选中图层没有可见填充色。请选择一个纯色填充图层。",
    };
  }

  if (!firstSolidFill) {
    return {
      ok: false,
      message: "请选择纯色图层；当前选中图层没有可读取的纯色填充。",
    };
  }

  return {
    ok: true,
    hex: figmaColorToHex(firstSolidFill.color),
  };
}

