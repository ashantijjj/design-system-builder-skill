function normalizeHex(value, fallback) {
  const raw = String(value || "").trim().replace(/^#/, "");
  if (/^[0-9a-fA-F]{3}$/.test(raw)) {
    return `#${raw.split("").map((char) => char + char).join("").toUpperCase()}`;
  }
  if (/^[0-9a-fA-F]{6}$/.test(raw)) return `#${raw.toUpperCase()}`;
  return fallback;
}

function hasValidHex(value) {
  const raw = String(value || "").trim().replace(/^#/, "");
  return /^[0-9a-fA-F]{3}$/.test(raw) || /^[0-9a-fA-F]{6}$/.test(raw);
}

function sanitizeTokenSegment(value) {
  const segment = String(value || "")
    .trim()
    .replace(/[\\/]+/g, "-")
    .replace(/\s+/g, "-")
    .replace(/^-+|-+$/g, "");
  return segment || "item";
}

function sanitizeModeName(value) {
  const name = String(value || "")
    .trim()
    .replace(/[\\/]+/g, "-")
    .replace(/\s+/g, " ");
  return name || "Theme";
}

function uniqueNames(names) {
  const seen = new Map();
  return names.map((name) => {
    const base = name || "Theme";
    const count = (seen.get(base) || 0) + 1;
    seen.set(base, count);
    return count === 1 ? base : `${base} ${count}`;
  });
}

function uniquifyThemeNames(themes) {
  const names = uniqueNames(themes.map((theme, index) => theme.name || `Theme ${index + 1}`));
  return themes.map((theme, index) => ({ ...theme, name: names[index] }));
}

