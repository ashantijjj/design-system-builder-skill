function groupColorTokens(tokens) {
  const order = ["Brand", "Background", "Neutral", "Overlay", "Semantic", "Auxiliary", "Light", "Dark"];
  const groups = {};
  tokens.forEach((token) => {
    const group = colorGroupName(token.name);
    if (!groups[group]) groups[group] = [];
    groups[group].push(token);
  });
  return Object.fromEntries(
    Object.entries(groups).sort(([a], [b]) => {
      const ai = order.indexOf(a);
      const bi = order.indexOf(b);
      return (ai === -1 ? 99 : ai) - (bi === -1 ? 99 : bi) || a.localeCompare(b);
    }),
  );
}

function colorGroupName(name) {
  const parts = String(name || "").split("/");
  if (["Light", "Dark"].includes(parts[0]) && parts[1]) return parts[1];
  return parts[0] || "Color";
}

function shortTokenName(name) {
  const parts = String(name || "").split("/");
  if (["Light", "Dark"].includes(parts[0])) return `${parts[0]} / ${parts.slice(2).join("/")}`;
  return parts.slice(1).join("/") || name;
}

